    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed

        boolean llave = false;

        ValidarUsuarios Verifica = new ValidarUsuarios();

        llave = Verifica.RevisandoUsuario(this.txtUsuario.getText(), this.txtPass.getText());

        if (llave) {
            JOptionPane.showMessageDialog(rootPane, "Bienvenido " + Verifica.getUsuarioActual());
            Principal Inicial = new Principal(Verifica.getUsuarioActual());
            this.setVisible(!llave);
            Inicial.setVisible(llave);
            
           

        } else {
            JOptionPane.showMessageDialog(rootPane, "Datos incorrectos");
            this.txtUsuario.setText("");
            this.txtPass.setText("");
            this.txtUsuario.requestFocus();
            //Tarea el cursor debe de aparecer en txtUsuario... LISTO!!!
        }


    }//GEN-LAST:event_btnEntrarActionPerformed

